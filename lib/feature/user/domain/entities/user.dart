class User {
  final String uuid;
  final String name;
  final String lastName;
  final String phoneNumber;
  final String email;
  final String password;
  final bool status;
  User({
    required this.uuid,
    required this.name,
    required this.lastName,
    required this.phoneNumber,
    required this.email,
    required this.password,
    required this.status
  });

}