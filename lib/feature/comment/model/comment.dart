class Comment {
  final String uuid;
  final String idUser;
  final String idPublic;
  final String text;
  final String userName;
  final String userNickName;

  Comment({
    required this.uuid,
    required this.idUser,
    required this.idPublic,
    required this.text,
    required this.userName,
    required this.userNickName,
  });
}